#include "libft.h"

int	ft_strcmp(const char *s1, const char *s2)
{
	while (1)
	{
		if (*s1 != *s2)
			return (*s1 - *s2);
		if (!*s1)
			break ;
		s1++;
		s2++;
	}
	return (0);
}
